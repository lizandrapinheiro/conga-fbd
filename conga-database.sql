-- 1.1. Criação dos tipos de Cargos (roles) que o Usuário pode ter (ENUM)
CREATE TYPE CARGOS_USUARIO_ENUM AS ENUM (
	'ADMIN', 
	'COLABORADOR'
);

-- 1.2. Tabela de Usuários
CREATE TABLE USUARIO (
	id_usuario INTEGER 				PRIMARY KEY,
	username   VARCHAR(100) 		NOT NULL UNIQUE,
	senha      VARCHAR(100) 		NOT NULL,
	nome       VARCHAR(100) 		NOT NULL,
	cargo      CARGOS_USUARIO_ENUM	NOT NULL
);

-- 2. Tabela de Logs de Acesso
CREATE TABLE LOG_ACESSO (
	id_log 	   		 SERIAL 		PRIMARY KEY,
	id_usuario 		 INTEGER	 	NOT NULL,
	hora       		 TIME		 	NOT NULL,
	data       		 DATE 			NOT NULL,
	status_tentativa VARCHAR 		NOT NULL,
	ip_origem		 VARCHAR,
	FOREIGN KEY (id_usuario) REFERENCES USUARIO(id_usuario)
);

-- 3.1 Criação dos tipos de despesa possíveis
CREATE TYPE TIPO_DESPESA_ENUM AS ENUM (
	'PESSOAL', 
	'UTILIDADE', 
	'INSUMO',
	'OPERACIONAL',
	'OUTROS'
);

-- 3.2 Tabela de Despesas
CREATE TABLE DESPESA (
	id_despesa 	 INTEGER 			PRIMARY KEY,
	id_usuario 	 INTEGER 			NOT NULL,
	data	   	 DATE	   			NOT NULL,
	valor	   	 DECIMAL 			NOT NULL,
	descricao	 VARCHAR(100),
	tipo_despesa TIPO_DESPESA_ENUM	NOT NULL,
	FOREIGN KEY (id_usuario) REFERENCES USUARIO(id_usuario)
);

-- 4. Tabela de Despesas com Pessoal
CREATE TABLE DESPESA_PESSOAL (
	id_despesa   	INTEGER			PRIMARY KEY,
	tipo_pessoal 	VARCHAR(100)	NOT NULL,
	FOREIGN KEY (id_despesa) REFERENCES DESPESA(id_despesa)
);

-- 5. Tabela de Despesas com Utilidades
CREATE TABLE DESPESA_UTILIDADE (
	id_despesa     	INTEGER			PRIMARY KEY,
	tipo_utilidade 	VARCHAR(100)	NOT NULL,
	FOREIGN KEY (id_despesa) REFERENCES DESPESA(id_despesa)
);

-- 6. Tabela de Despesas com Insumos
CREATE TABLE DESPESA_INSUMO (
	id_despesa   	INTEGER			PRIMARY KEY,
	nome_insumo  	VARCHAR(100)	NOT NULL,
	quantidade		DECIMAL			NOT NULL,
	preco_unitario	DECIMAL			NOT NULL,
	FOREIGN KEY (id_despesa) REFERENCES DESPESA(id_despesa)
);

-- 7. Tabela de Despesas com Operacional
CREATE TABLE DESPESA_OPERACIONAL (
	id_despesa   	 INTEGER		PRIMARY KEY,
	tipo_operacional VARCHAR(100)	NOT NULL,
	FOREIGN KEY (id_despesa) REFERENCES DESPESA(id_despesa)
);

-- 8. Tabela de Despesas com Outros
CREATE TABLE DESPESA_OUTROS (
	id_despesa   	 INTEGER		PRIMARY KEY,
	FOREIGN KEY (id_despesa) REFERENCES DESPESA(id_despesa)
);

-- 9. Tabela de Preço de Venda
CREATE TABLE PRECO_VENDA (
	id_preco_venda	 	 INTEGER 	PRIMARY KEY,
	valor_unitario	 	 DECIMAL	NOT NULL,
	data_vigencia_inicio DATE		NOT NULL,
	data_vigencia_fim	 DATE
);

-- 10. Tabela de Produção do Produtor
CREATE TABLE PRODUCAO_PRODUTOR (
	id_producao_produtor INTEGER 	PRIMARY KEY,
	id_usuario			 INTEGER 	NOT NULL,
	quantidade_litro	 DECIMAL	NOT NULL,
	data_producao		 DATE		NOT NULL,
	FOREIGN KEY (id_usuario) REFERENCES USUARIO(id_usuario)
);

-- 11. Tabela de Produção Local
CREATE TABLE PRODUCAO_LOCAL (
	id_producao_local	 	 INTEGER 	PRIMARY KEY,
	id_usuario			 	 INTEGER 	NOT NULL,
	volume_bruto		 	 DECIMAL	NOT NULL,
	consumo_interno		 	 DECIMAL	NOT NULL,
	quantidade_total_litro	 DECIMAL	NOT NULL,
	data_producao		 	 DATE		NOT NULL,
	FOREIGN KEY (id_usuario) REFERENCES USUARIO(id_usuario)
);

-- 10. Tabela para o Cálculo de Receita
CREATE TABLE CALCULO_RECEITA (
	id_calculo				 INTEGER 	PRIMARY KEY,
	id_producao_produtor	 INTEGER	NOT NULL,
	id_producao_local		 INTEGER 	NOT NULL,
	id_preco_venda			 INTEGER	NOT NULL,
	FOREIGN KEY (id_producao_produtor) REFERENCES PRODUCAO_PRODUTOR(id_producao_produtor),
	FOREIGN KEY (id_producao_local) REFERENCES PRODUCAO_LOCAL(id_producao_local),
	FOREIGN KEY (id_preco_venda) REFERENCES PRECO_VENDA(id_preco_venda)
);

