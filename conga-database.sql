-- 1. Usuário com mais tentativas de login falhas
SELECT u.nome, COUNT(*) AS falhas
FROM LOG_ACESSO l
JOIN USUARIO u ON u.id_usuario = l.id_usuario
WHERE l.status_tentativa = 'FALHA'
GROUP BY u.nome
ORDER BY falhas DESC;

-- 2. IPs de origem com mais tentativas falhas de login
SELECT l.ip_origem, COUNT(*) AS falhas
FROM LOG_ACESSO l
WHERE l.status_tentativa = 'FALHA'
GROUP BY l.ip_origem
ORDER BY falhas DESC;

-- 3. Consulta ordenada do valor total de despesas por tipo
SELECT tipo_despesa, SUM(valor) AS total
FROM DESPESA
GROUP BY tipo_despesa
ORDER BY total DESC;

-- 4. Ranking de despesas pessoais por tipo
SELECT dp.tipo_pessoal, COUNT(*) AS qtd_registros, SUM(d.valor) AS total
FROM DESPESA_PESSOAL dp
JOIN DESPESA d ON d.id_despesa = dp.id_despesa
GROUP BY dp.tipo_pessoal
ORDER BY total DESC;

-- 5. Ranking de despesas com utilidades por tipo
SELECT du.tipo_utilidade, COUNT(*) AS qtd_registros, SUM(d.valor) AS total
FROM DESPESA_UTILIDADE du
JOIN DESPESA d ON d.id_despesa = du.id_despesa
GROUP BY du.tipo_utilidade
ORDER BY total DESC;

-- 6. Ranking de despesas com insumos por tipo
SELECT di.nome_insumo AS insumo, COUNT(*) AS qtd_registros, 
		SUM(di.quantidade) AS total_quantidade, 
		SUM(di.preco_unitario) AS total_preco_unitario,
       (SUM(di.quantidade) * SUM(di.preco_unitario)) AS custo_total
FROM DESPESA_INSUMO di
GROUP BY di.nome_insumo
ORDER BY custo_total DESC;

-- 7. Ranking de despesas com operacional por tipo
SELECT dop.tipo_operacional, COUNT(*) AS qtd_registros, SUM(d.valor) AS total
FROM DESPESA_OPERACIONAL dop
JOIN DESPESA d ON d.id_despesa = dop.id_despesa
GROUP BY dop.tipo_operacional
ORDER BY total DESC;

-- 8. Quantidade total do tanque (Produtor + Local)
SELECT 
    pp.quantidade_litro AS produtor,
    pl.quantidade_total_litro AS local,
	(pp.quantidade_litro + pl.quantidade_total_litro) AS tanque,
    pp.data_producao
FROM CALCULO_RECEITA c
JOIN PRODUCAO_PRODUTOR pp ON pp.id_producao_produtor = c.id_producao_produtor
JOIN PRODUCAO_LOCAL pl ON pl.id_producao_local = c.id_producao_local
ORDER BY pp.data_producao;

-- 9. Consulta ordenada de colaboradores que mais registraram produção de leite 
SELECT u.nome AS colaborador, SUM(pp.quantidade_litro) AS total_litros
FROM PRODUCAO_PRODUTOR pp
JOIN USUARIO u ON u.id_usuario = pp.id_usuario
GROUP BY u.nome
ORDER BY total_litros DESC;

-- 10. Receita potencial total calculada
SELECT 
    c.id_calculo,
	pv.valor_unitario as preco_venda,
	(pp.quantidade_litro + pl.quantidade_total_litro) AS tanque,
    (pp.quantidade_litro + pl.quantidade_total_litro) * pv.valor_unitario
        AS receita_calculada
FROM CALCULO_RECEITA c
JOIN PRODUCAO_PRODUTOR pp ON pp.id_producao_produtor = c.id_producao_produtor
JOIN PRODUCAO_LOCAL pl ON pl.id_producao_local = c.id_producao_local
JOIN PRECO_VENDA pv ON pv.id_preco_venda = c.id_preco_venda;

-- 11. Receita média diária
SELECT AVG((pp.quantidade_litro + pl.quantidade_total_litro) * pv.valor_unitario) AS receita_media
FROM CALCULO_RECEITA c
JOIN PRODUCAO_PRODUTOR pp ON pp.id_producao_produtor = c.id_producao_produtor
JOIN PRODUCAO_LOCAL pl ON pl.id_producao_local = c.id_producao_local
JOIN PRECO_VENDA pv ON pv.id_preco_venda = c.id_preco_venda

-- 12. Maior receita obtida em um único dia
SELECT pv.data_vigencia_inicio AS data,
    (pp.quantidade_litro + pl.quantidade_total_litro) * pv.valor_unitario AS receita
FROM CALCULO_RECEITA c
JOIN PRODUCAO_PRODUTOR pp ON pp.id_producao_produtor = c.id_producao_produtor
JOIN PRODUCAO_LOCAL pl ON pl.id_producao_local = c.id_producao_local
JOIN PRECO_VENDA pv ON pv.id_preco_venda = c.id_preco_venda
